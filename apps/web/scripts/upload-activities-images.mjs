import { readFileSync, writeFileSync } from "node:fs";
import { S3Client, PutObjectCommand, ListObjectsV2Command } from "@aws-sdk/client-s3";
import { requireEnv } from "./_env.mjs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const MANIFEST_PATH = join(here, "..", "src", "data", "asset-manifest.ts");

const env = requireEnv(
  "R2_ACCOUNT_ID", "R2_ACCESS_KEY_ID", "R2_SECRET_ACCESS_KEY",
  "R2_BUCKET", "R2_S3_ENDPOINT", "NEXT_PUBLIC_R2_BASE_URL",
);

const s3 = new S3Client({
  region: "auto",
  endpoint: env.R2_S3_ENDPOINT,
  credentials: {
    accessKeyId: env.R2_ACCESS_KEY_ID,
    secretAccessKey: env.R2_SECRET_ACCESS_KEY,
  },
});

async function listBucket() {
  const map = new Map();
  let token;
  do {
    const res = await s3.send(new ListObjectsV2Command({
      Bucket: env.R2_BUCKET, ContinuationToken: token,
    }));
    for (const o of res.Contents ?? []) {
      map.set(o.Key, { size: o.Size, etag: (o.ETag ?? "").replace(/"/g, "") });
    }
    token = res.IsTruncated ? res.NextContinuationToken : undefined;
  } while (token);
  return map;
}

function writeManifest(keys) {
  const base = env.NEXT_PUBLIC_R2_BASE_URL.replace(/\/$/, "");
  const sorted = [...keys].sort();
  const body = sorted.map((k) => `  ${JSON.stringify(k)}: ${JSON.stringify(`${base}/${k}`)},`).join("\n");
  const out = `// AUTO-GENERATED by scripts/upload-assets.mjs — do not edit by hand.
// Maps an asset key to its public R2 URL. Reference via asset() in src/lib/assets.ts.
export const manifest = {
${body}
} as const;

export type AssetKey = keyof typeof manifest;
`;
  writeFileSync(MANIFEST_PATH, out);
}

async function upload() {
  const tempDir = join(here, "..", "temp_activities_img");

  for (let i = 1; i <= 9; i++) {
    const webpName = `student-activities-0${i}.webp`;
    const pngName = `student-activities-0${i}.png`;

    const webpKey = `departments/civil/${webpName}`;
    const pngKey = `departments/civil/${pngName}`;

    const webpBody = readFileSync(join(tempDir, webpName));
    const pngBody = readFileSync(join(tempDir, pngName));

    console.log(`Uploading ${webpKey}...`);
    await s3.send(new PutObjectCommand({
      Bucket: env.R2_BUCKET,
      Key: webpKey,
      Body: webpBody,
      ContentType: "image/webp",
    }));

    console.log(`Uploading ${pngKey}...`);
    await s3.send(new PutObjectCommand({
      Bucket: env.R2_BUCKET,
      Key: pngKey,
      Body: pngBody,
      ContentType: "image/png",
    }));
  }

  console.log("Listing bucket to update asset manifest...");
  const bucketMap = await listBucket();
  writeManifest(bucketMap.keys());
  console.log(`Manifest updated with ${bucketMap.size} keys!`);
}

upload().catch((err) => {
  console.error("Upload failed:", err);
  process.exit(1);
});
